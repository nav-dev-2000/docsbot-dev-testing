import { configureFirebaseApp } from '@/config/firebase-server.config'
import { getBot } from '@/lib/dbQueries'
import userTeamCheck from '@/lib/userTeamCheck'
import { canUserManageIntegrations } from '@/utils/function.utils'
import { WEBHOOK_EVENT_DEEP_RESEARCH_DONE } from '@/lib/webhooks'
import { deliverWebhookEvent } from '@/lib/webhookDelivery'

const now = new Date().toISOString()

const TEST_RESEARCH_PAYLOAD = {
  event: WEBHOOK_EVENT_DEEP_RESEARCH_DONE,
  research: {
    jobId: 'test_research_sample',
    status: 'completed',
    title: 'Test deep research task',
    question: 'Sample research question for webhook test',
    createdAt: now,
    completedAt: now,
    metadata: null,
    answer:
      '## Summary\n\nThis is a sample deep research answer in markdown format.\n\n### Key findings\n\n1. First finding with supporting details.\n2. Second finding from the analysis.\n3. Third finding and recommendations.\n\n---\n*Generated by DocsBot Deep Research*',
  },
}

export default async function handler(req, res) {
  configureFirebaseApp()

  if (req.method !== 'POST') {
    return res.status(400).json({ message: 'Invalid HTTP method' })
  }

  let check = null
  try {
    check = await userTeamCheck(req, res)
  } catch (error) {
    return res.status(403).json({ message: error?.message })
  }

  const { userId, team } = check
  const { botId } = req.query
  const { webhookId } = req.body || {}

  const bot = await getBot(team.id, botId)
  if (!bot) {
    return res.status(404).json({ message: "botId doesn't exist." })
  }

  if (!canUserManageIntegrations(team, userId, bot)) {
    return res.status(403).json({
      message:
        'You are not allowed to deliver research webhooks for this bot.',
    })
  }

  const payload = {
    ...TEST_RESEARCH_PAYLOAD,
    teamId: team.id,
    botId,
  }

  const result = await deliverWebhookEvent({
    teamId: team.id,
    botId,
    event: WEBHOOK_EVENT_DEEP_RESEARCH_DONE,
    payload,
    webhookId,
  })

  return res.status(200).json(result)
}
