export const HERO_ICON_SET = new Set(HERO_ICON_ENUM)
export const DEFAULT_CUSTOM_BUTTON_ICON = 'LinkIcon'

export const normalizeWhitelistedHeroIcon = (
  value,
  fallback = DEFAULT_CUSTOM_BUTTON_ICON,
) => {
  const normalizedValue = typeof value === 'string' ? value.trim() : ''

  if (HERO_ICON_SET.has(normalizedValue)) {
    return normalizedValue
  }

  return HERO_ICON_SET.has(fallback) ? fallback : DEFAULT_CUSTOM_BUTTON_ICON
}
